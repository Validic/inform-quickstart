// Map to track which names have been assigned to which user IDs
const nameAssignments = new Map<string, string>();
let nextNameIndex = 0;

/**
 * Get a consistent display name for a user ID.
 * The same user ID will always return the same name within a session.
 */
export function getDisplayName(userId: string): string {
  // Check if we already assigned a name to this user
  if (nameAssignments.has(userId)) {
    return nameAssignments.get(userId)!;
  }

  // Assign the next available name
  const name = DISPLAY_NAMES[nextNameIndex % DISPLAY_NAMES.length];
  nameAssignments.set(userId, name);
  nextNameIndex++;

  return name;
}

